function valid_cvb(fileid)

use Definitions, only: wp, iwp

implicit none
logical(kind=iwp) :: valid_cvb
real(kind=wp), intent(in) :: fileid

valid_cvb = (fileid >= 1.0e-2_wp)

return

end function valid_cvb
